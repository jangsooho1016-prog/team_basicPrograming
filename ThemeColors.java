import java.awt.Color;

/**
 * ========================================================
 * [핵심] 공통 테마 색상 (Theme Colors)
 * ========================================================
 * 어플리케이션 전체에서 사용되는 색상 팔레트입니다.
 * '바나나' 컨셉에 맞춘 노란색 계열의 색상들을 정의하고 있습니다.
 * 유지보수를 위해 색상 코드를 이곳에서 한 번에 관리합니다.
 */
public class ThemeColors {
    public static final Color BG = new Color(255, 250, 205); // 배경색 (연한 크림) - Light Cream
    public static final Color MAIN = new Color(255, 225, 53); // 메인 색상 (바나나 노랑) - Banana Yellow
    public static final Color DARK = new Color(139, 69, 19); // 어두운 색상 (초콜릿/갈색) - Chocolate Brown
    public static final Color HIGHLIGHT = new Color(255, 240, 150); // 하이라이트 색상 (밝은 노랑) - Light Yellow
    public static final Color ACCENT = new Color(255, 180, 0); // 강조 색상 (진한 노랑/오렌지) - Deep Yellow
    public static final Color TEXT = new Color(100, 50, 0); // 텍스트 색상 (짙은 갈색) - Dark Brown
}
